import streamlit as st

# ====================================================
# 1. 페이지 기본 설정 및 큰 제목 (지시사항 1번 반영)
# ====================================================
st.set_page_config(page_title="3학년 2학기 수학: 들이와 무게 마스터", page_icon="⚖️", layout="centered")

st.title("⚖️ 초등학교 3학년 2학기 5단원: 들이와 무게")
st.write("단순히 공식만 외우지 말고, 직접 조작하며 들이와 무게의 감각을 익혀보세요.")
st.write("---")

# ====================================================
# 2. 세션 상태(session_state) 초기화 확인
# ====================================================
if "user_name_input" not in st.session_state:
    st.session_state["user_name_input"] = ""

# 초기화 콜백: 세션 상태를 초기화하고 리런합니다.
def _reset_app():
    st.session_state["user_name_input"] = ""
    # 필요한 추가 상태가 있으면 여기서 초기화하세요.
    st.rerun()

# ====================================================
# 3. 사용자 이름 입력창 (첫 진입 화면 - 지시사항 1번 반영)
# ====================================================
user_name = st.text_input(
    "👋 반갑습니다! 여러분의 이름을 입력해주세요:", 
    placeholder="예: 홍길동",
    key="user_name_input"
)

# 이름을 입력해야만 하단의 4개 학습 페이지(탭)가 활성화됩니다.
if user_name.strip():
    st.success(f"🎉 준비 완료! **{user_name}** 학생만을 위한 맞춤형 '들이와 무게' 교실을 시작합니다.")
    
    # 지시사항에 따른 4개 페이지 구성 (탭 구조)
    menu = st.tabs([
        "📋 [1페이지] 실생활 속 단위 발견", 
        "📖 [2페이지] 교과서 개념 정리방", 
        "🔍 [3페이지] 자릿수 추론 조작실", 
        "✏️ [4페이지] 단위 마스터 퀴즈방"
    ])
    
    # ----------------------------------------------------
    # [1페이지: 실생활 속 단위 발견] (지시사항 2번 - 첫번째 내용)
    # ----------------------------------------------------
    with menu[0]:
        st.header("📋 1페이지: 우리 주변에서 들이와 무게를 사용하는 상황")
        st.write(f"**{user_name}**님, 일상생활에서 다양한 단위가 쓰이는 상황을 먼저 살펴봐요!")
        
        col1, col2 = st.columns(2)
        with col1:
            st.subheader("🧪 들이를 나타낼 때")
            st.info("🥛 **욕조 가득 물의 양**\n\n우리가 매일 사용하는 **욕조 한 가득의 물**은 약 **300 L**나 됩니다. mL로 나타내면 자릿수가 어떻게 될까요?")
            st.warning("🍼 **우유 한 팩의 양**\n\n급식으로 먹는 작은 우유 한 팩은 **200 mL**입니다. 이것을 큰 단위인 **L**로 바꾸어 표현해 볼까요?")
            
        with col2:
            st.subheader("⚖️ 무게를 나타낼 때")
            st.info("🐋 **흰수염고래의 무게**\n\n지구상에서 가장 큰 동물은 약 **150000 kg**입니다. 단위를 **g**으로 쓰면 숫자가 너무 커지겠죠?")
            st.warning("🍎 **사과 한 개의 무게**\n\n보통 사과 한 개는 약 **250 g**입니다. 마트에서 사과를 많이 골라 담아 **kg** 단위가 되면 어떻게 변할지 알아봅시다.")

    # ----------------------------------------------------
    # [2페이지: 교과서 개념 정리방] (피드백 반영: 비교하기 빼고 단위 알아보기만 구성)
    # ----------------------------------------------------
    with menu[1]:
        st.header("📖 2페이지: 교과서 핵심 개념 정리")
        st.write("보내주신 교과서 사진의 핵심 단위를 바탕으로 개념을 차근차근 정리해 봅시다.")
        st.write("---")
        
        concept_choice = st.selectbox("공부할 개념을 선택하세요:", ["1. 들이의 단위 알아보기", "2. 무게의 단위 알아보기"])
        
        # 2-1. 들이의 단위 알아보기 (들이.png 파일 반영)
        if concept_choice == "1. 들이의 단위 알아보기":
            st.subheader("🧪 들이의 단위 (L와 mL) 알아보기")
            st.success("💡 **교과서 핵심 약속**\n* 들이의 단위에는 **리터($L$)**와 **밀리리터($mL$)** 등이 있습니다.\n* $1$ 리터는 **$1\\text{ L}$**, $1$ 밀리리터는 **$1\\text{ mL}$**라고 씁니다.\n* **$1\\text{ L} = 1000\\text{ mL}$** 와 같습니다.")
            
            st.markdown("""
            * **$1\\text{ L } 300\\text{ mL}$ 알아보기 (복명수 변환):**
              - $1\\text{ L}$보다 $300\\text{ mL}$ 더 많은 들이를 **$1\\text{ L } 300\\text{ mL}$**이라 쓰고, **'일 리터 삼백 밀리리터'**라고 읽습니다.
              - $1\\text{ L}$은 $1000\\text{ mL}$와 같으므로, **$1\\text{ L } 300\\text{ mL} = 1300\\text{ mL}$**입니다.
            """)
            
        # 2-2. 무게의 단위 알아보기 (무게.png 파일 반영)
        elif concept_choice == "2. 무게의 단위 알아보기":
            st.subheader("⚖️ 무게의 단위 (g, kg, t) 알아보기")
            st.success("💡 **교과서 핵심 약속 1**\n* 무게의 단위에는 **킬로그램($kg$)**과 **그램($g$)** 등이 있습니다.\n* $1$ 킬로그램은 **$1\\text{ kg}$**, $1$ 그램은 **$1\\text{ g}$**이라고 씁니다.\n* **$1\\text{ kg} = 1000\\text{ g}$** 과 같습니다.")
            
            st.markdown("""
            * **$1\\text{ kg } 400\\text{ g}$ 알아보기 (복명수 변환):**
              - $1\\text{ kg}$보다 $400\\text{ g}$ 더 무거운 무게를 **$1\\text{ kg } 400\\text{ g}$**이라 쓰고, **'일 킬로그램 사백 그램'**이라고 읽습니다.
              - $1\\text{ kg}$은 $1000\\text{ g}$과 같으므로, **$1\\text{ kg } 400\\text{ g} = 1400\\text{ g}$**입니다.
            """)
            
            st.info("💡 **교과서 핵심 약속 2 (톤 알아보기)**\n* **$1000\\text{ kg}$**의 무게를 **$1\\text{ t}$**이라 쓰고 **'일 톤'**이라고 읽습니다.\n* **$1\\text{ t} = 1000\\text{ kg}$** 의 관계를 가집니다.")

    # ----------------------------------------------------
    # [3페이지: 자릿수 추론 조작실] (여기에만 조작실 코딩이 깔끔하게 들어감)
    # ----------------------------------------------------
    with menu[2]:
        st.header("🔍 3페이지: 손으로 움직이며 추론하는 조작실")
        st.write("슬라이더를 직접 움직이면서 수의 크기와 단위가 어떻게 변하는지 눈으로 관찰해보세요.")
        
        unit_choice = st.selectbox("어떤 단위를 조작해볼까요?", ["들이 단위 (mL ↔ L)", "무게 단위 (g ↔ kg)"])
        
        if unit_choice == "들이 단위 (mL ↔ L)":
            st.subheader("🧪 들이의 양방향 변화 관찰")
            l_slide = st.slider("👉 L(리터) 값을 움직이며 mL 값이 어떻게 변하는지 보세요:", 0.0, 5.0, 1.3, step=0.1)
            ml_calc = int(l_slide * 1000)
            
            st.metric(label="🥛 큰 단위 (L)", value=f"{l_slide} L")
            st.markdown("⬇ **단위가 작아지면 수의 크기는 1000배가 됩니다! (소수점이 오른쪽으로 3칸 이동)**")
            st.metric(label="💧 작은 단위 (mL)", value=f"{ml_calc} mL")
            st.write("---")
            st.markdown("💡 **반대로 생각하기 (mL ➡️ L)**")
            st.write("작은 단위(mL)에서 큰 단위(L)로 바뀔 때는 수의 크기가 **1/1000배**로 줄어듭니다. 소수점이 왼쪽으로 3칸 이동하는 규칙을 기억하세요!")

        elif unit_choice == "무게 단위 (g ↔ kg)":
            st.subheader("⚖️ 무게의 양방향 변화 관찰")
            kg_slide = st.slider("👉 kg 값을 움직이며 g 값이 어떻게 변하는지 보세요:", 0.0, 5.0, 1.4, step=0.1)
            g_calc = int(kg_slide * 1000)
            
            st.metric(label="🍏 큰 단위 (kg)", value=f"{kg_slide} kg")
            st.markdown("⬇ **단위가 작아지면 수의 크기는 1000배가 됩니다! (자릿수가 위로 3칸 올라갑니다)**")
            st.metric(label="🍬 작은 단위 (g)", value=f"{g_calc} g")
            st.write("---")
            st.markdown("💡 **반대로 생각하기 (g ➡️ kg)**")
            st.write("작은 단위(g)에서 큰 단위(kg)로 바뀔 때는 수가 **1/1000배**로 작아지므로 자릿수가 아래로 3칸 내려가게 됩니다.")

    # ----------------------------------------------------
    # [4페이지: 단위 마스터 퀴즈방 및 하단 초기화] (4페이지 영역에 독립적으로 분리 완료)
    # ----------------------------------------------------
    with menu[3]:
        st.header(f"✏️ 4페이지: {user_name}이를 위한 들이·무게 도전 퀴즈!")
        st.write("교과서 개념 정리방과 조작실에서 배운 원리를 생각하며 퀴즈를 풀어보세요.")
        
        quiz_select = st.selectbox("풀고 싶은 퀴즈 유형을 선택하세요:", [
            "Q1. 교과서 들이 단위 변환 문제 ", 
            "Q2. 들이 단위 변환 (mL ➡️ L)",
            "Q3. 교과서 무게 단위 변환 문제", 
            "Q4. 무게 단위 변환 (g ➡️ kg)",
            "Q5. 교과서 톤(t) 단위 변환 문제"
        ])
        
        st.write("---")
        
        # Q1. 들이 단위 변환 문제 (L, mL -> mL)
        if quiz_select == "Q1. 교과서 들이 단위 변환 문제 ":
            st.subheader("🎯 들이 단위 변환 퀴즈")
            st.write("🔍 **문제:** 교과서에 나온 예시입니다. **1 L 300 mL**는 몇 mL일까요?")
            ans1 = st.text_input("숫자만 입력하세요:", placeholder="여기에 정답을 입력하세요", key="ans_q1")
            
            if st.button("정답 확인하기", key="btn_q1"):
                if ans1.strip() == "1300":
                    st.success(f"🎉 대단해요, {user_name}님! 정답입니다! 1 L가 1000 mL인 원리를 잘 적용했어요!")
                    st.balloons()
                else:
                    st.error("아쉬워요! 다시 생각해보세요. 1 L는 1000 mL이므로 300 mL를 더해주면 됩니다.")
                    
        # Q2. mL -> L
        elif quiz_select == "Q2. 들이 단위 변환 (mL ➡️ L)":
            st.subheader("🎯 들이 단방향 변환 퀴즈")
            st.write("🔍 **문제:** 개인 텀블러에 담긴 보리차의 양을 재어보니 **750 mL**였습니다. 이것은 몇 L일까요?")
            ans2 = st.text_input("소수점을 정확히 찍어주세요 ):", placeholder="여기에 정답을 입력하세요", key="ans_q2")
            
            if st.button("정답 확인하기", key="btn_q2"):
                if ans2.strip() == "0.75":
                    st.success(f"🎉 정답입니다, {user_name}님! 자릿수를 왼쪽으로 3칸 알맞게 이동했네요!")
                    st.balloons()
                else:
                    st.error("오답입니다. mL에서 L로 갈 때는 1000으로 나누어 소수점을 왼쪽으로 3칸 옮겨야 합니다.")
                    
        # Q3. 무게 변환 (kg, g -> g)
        elif quiz_select == "Q3. 교과서 무게 단위 변환 문제":
            st.subheader("🎯 무게 단위 변환 퀴즈")
            st.write("🔍 **문제:** 교과서에 나온 예시입니다. 사과 한 상자의 무게인 **1 kg 400 g**은 몇 g일까요?")
            ans3 = st.text_input("숫자만 입력하세요:", placeholder="여기에 정답을 입력하세요", key="ans_q3")
            
            if st.button("정답 확인하기", key="btn_q3"):
                if ans3.strip() == "1400":
                    st.success(f"🎉 정답입니다! 1 kg이 1000 g이므로 400 g을 더해 1400 g이 됩니다!")
                    st.balloons()
                else:
                    st.error("다시 계산해보세요! 1 kg은 1000 g과 같습니다.")

        # Q4. g -> kg
        elif quiz_select == "Q4. 무게 단위 변환 (g ➡️ kg)":
            st.subheader("🎯 무게 단방향 변환 퀴즈")
            st.write("🔍 **문제:** 집으로 배달된 택배 상자의 무게를 측정하니 **3200 g**이 나왔습니다. 이 상자는 몇 kg일까요?")
            ans4 = st.text_input("소수점을 포함하여 입력하세요:", key="ans_q4")
            
            if st.button("정답 확인하기", key="btn_q4"):
                if ans4.strip() == "3.2":
                    st.success("🎉 완벽합니다! 자릿수가 아래로 3칸 내려가는 원리를 완벽하게 이해하셨네요!")
                    st.balloons()
                else:
                    st.error("틀렸습니다. g에서 kg으로 바뀔 때는 수의 크기가 1/1000배가 됩니다.")

        # Q5. 톤 단위 변환 (t -> kg)
        elif quiz_select == "Q5. 교과서 톤(t) 단위 변환 문제":
            st.subheader("🎯 고난도 톤(t) 변환 퀴즈")
            st.write("🔍 **문제:** 교과서에 나온 새로운 무게 단위입니다. 트럭에 실린 짐의 무게가 **1 t**이라면, 이것은 몇 kg과 같을까요?")
            ans5 = st.text_input("숫자만 입력하세요:", placeholder="여기에 정답을 입력하세요", key="ans_q5")
            
            if st.button("정답 확인하기", key="btn_q5"):
                if ans5.strip() == "1000":
                    st.success("🎉 정답입니다! 1000 kg이 모이면 아주 무거운 무게 단위인 1 t이 된다는 것을 잘 맞추셨습니다!")
                    st.balloons()
                else:
                    st.error("아쉬워요! 교과서 개념 정리방에서 1 톤(t)이 몇 kg이었는지 다시 확인해 보세요.")

        # ----------------------------------------------------
        # 4페이지 하단: 전체 화면 초기화 시스템 (지시사항 2번 - 하단 초기화 버튼)
        # ----------------------------------------------------
        st.write("---")
        st.write("💡 처음부터 다시 학습하거나 화면을 리셋하려면 아래 버튼을 누르세요.")
        st.button("🔄 전체 화면 초기화하고 처음으로 돌아가기", on_click=_reset_app)

else:
    # 사용자 이름을 적기 전, 가장 첫 진입 화면 레이아웃
    st.info("💡 위의 입력창에 이름을 입력하시면 3학년 2학기 '들이와 무게' 조작 교실이 시작됩니다!")